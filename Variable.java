public class Variable {
    public static void main(String[] args) {
        byte cores = 4; 
        short memory = 16;
        int typeSystem = 64;
        long soket = 1155;
        float coreVoltage = 1.184f;
        double freq = 1199.8d;
        char cpu = 'i';
        boolean comp = true;
        
        System.out.println("byte cores = " + cores);
        System.out.println("short memory = " + memory);
        System.out.println("int typeSystem = " + typeSystem);
        System.out.println("long soket = " + soket);
        System.out.println("float coreVoltage = " + coreVoltage);
        System.out.println("double freq = " + freq);
        System.out.println("char cpu = " + cpu);
        System.out.println("boolean comp = " + comp);
    }
}